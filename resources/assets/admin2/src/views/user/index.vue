<template>
  <div class="qingwu">
    <div class="admin_table_page_title">平台用户列表</div>
    <div class="unline underm"></div>

    <div class="admin_table_handle_btn">
      <a-button @click="$router.push('/Admin/users/form')" type="primary" icon="plus">添加</a-button>
      <a-button class="admin_delete_btn" type="danger" icon="delete" @click="del">批量删除</a-button>
    </div>
    <div class="admin_table_list">
      <a-table
        :columns="columns"
        :data-source="list"
        :pagination="false"
        :row-selection="{ selectedRowKeys: selectedRowKeys, onChange: onSelectChange }"
        row-key="id">
        <span slot="action" slot-scope="rows">
          <a-button icon="edit" @click="$router.push('/Admin/users/form/'+rows.id)">编辑</a-button>
        </span>
      </a-table>
      <div class="admin_pagination" v-if="total>0">
        <a-pagination
          v-model="params.page"
          :page-size.sync="params.per_page"
          :total="total"
          @change="onChange"
          show-less-items />
      </div>
    </div>
  </div>
</template>

<script>
export default {
  components: {},
  props: {},
  data () {
    return {
      params: {
        page: 1,
        per_page: 30
      },
      total: 0, // 总页数
      selectedRowKeys: [], // 被选择的行
      columns: [
        { title: '#', dataIndex: 'id', fixed: 'left' },
        { title: '昵称', dataIndex: 'nickname' },
        { title: '手机', dataIndex: 'phone' },
        { title: '用户名', dataIndex: 'username' },
        { title: '登陆IP', dataIndex: 'ip' },
        { title: '登陆时间', dataIndex: 'login_time' },
        { title: '上次登陆', dataIndex: 'last_login_time' },
        { title: '创建时间', dataIndex: 'created_at' },
        { title: '操作', key: 'id', fixed: 'right', scopedSlots: { customRender: 'action' } }
      ],
      list: []
    }
  },
  watch: {},
  computed: {},
  methods: {
    // 选择框被点击
    onSelectChange (selectedRowKeys) {
      this.selectedRowKeys = selectedRowKeys
    },
    // 选择分页
    onChange (e) {
      this.params.page = e
    },
    // 删除
    del () {
      if (this.selectedRowKeys.length === 0) {
        return this.$message.error('未选择数据')
      }
      this.$confirm({
        title: '你确定要删除选择的数据？',
        content: '确定删除后无法恢复',
        okText: '确定',
        okType: 'danger',
        cancelText: '取消',
        onOk: () => {
          const ids = this.selectedRowKeys.join(',')
          this.$delete(this.$api.adminUsers + '/' + ids).then(res => {
            if (res.error_code === 0) {
              this.onload()
              this.$message.success('删除成功')
            } else {
              this.$message.error(res.msg)
            }
          })
        }
      })
    },

    onload () {
      this.$get(this.$api.adminUsers, this.params).then(res => {
        this.total = res.data.total
        this.list = res.data.data
      })
    }
  },
  created () {
    this.onload()
  },
  mounted () {
  }
}
</script>
