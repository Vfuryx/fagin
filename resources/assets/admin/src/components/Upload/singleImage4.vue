<template>
  <div class="upload-container">
    <el-upload
      :data="dataObj"
      :multiple="false"
      :show-file-list="false"
      :on-success="handleImageSuccess"
      :before-upload="beforeUpload"
      class="image-uploader"
      drag
      :action="uploadAction"
      :headers="headers"
    >
      <i class="el-icon-upload" />
      <div class="el-upload__text">
        <em>点击上传</em>
      </div>
      <div
        class="el-upload__tip"
        style="position: absolute; margin-top: -20px;"
        slot="tip"
      >{{ tip }}</div>
    </el-upload>
    <div class="image-preview">
      <div v-show="imageUrl.length>1" class="image-preview-wrapper">
        <img :src="imageUrl != '' ? baseURL + imageUrl: ''" />
        <div class="image-preview-action">
          <i class="el-icon-delete" @click="rmImage" />
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import { getToken } from '@/utils/auth'

function beforeUploadFunc(file) {
  const isJPG = file.type === 'image/jpeg'
  const isLt2M = file.size / 1024 / 1024 < 20

  if (!isJPG) {
    this.$message.error('上传图片只能是 JPG 格式!')
  }
  if (!isLt2M) {
    this.$message.error('上传图片大小不能超过 20MB!')
  }
  return isJPG && isLt2M
}

export default {
  name: 'SingleImageUpload',
  props: {
    value: {
      type: String,
      default: ''
    },
    tip: {
      type: String,
      default: ''
    },
    uploadAction: {
      type: String,
      default: ''
    },
    beforeUpload: {
      type: Function,
      default: beforeUploadFunc
    }
  },
  data() {
    return {
      baseURL: process.env.VUE_APP_SERVER_PUBLIC_PATH,
      tempUrl: '',
      dataObj: { token: '', key: '' },
      headers: {
        'Authorization': `Bearer ${getToken()}`
      }

    }
  },
  computed: {
    imageUrl() {
      return this.value
    }
  },
  methods: {
    rmImage() {
      this.emitInput('')
    },
    emitInput(val) {
      this.$emit('input', val)
    },
    handleImageSuccess(response, file, fileList) {
      this.tempUrl = response.data.path
      this.emitInput(this.tempUrl)
    }
  }
}
</script>

